# Test Writer Agent: Generate pytest test stubs and enhance with Gemini 1.5 Flash

import textwrap
import sys

from utils.config import get_gemini_api_key
from utils.gemini_client import gemini_chat


def run_test_writer(state: dict) -> dict:
    """Generate test stubs from analysis results and optionally enhance with Gemini."""
    analysis = state.get("analysis", {})
    if not isinstance(analysis, dict):
        analysis = {}

    modules = analysis.get("modules", []) or []
    tests = []

    for mod in modules:
        if not isinstance(mod, dict):
            continue
        for func in mod.get("functions", []):
            if not isinstance(func, dict):
                continue
            # Skip private/dunder helpers — they're rarely testable standalone
            fname = func.get("name", "")
            if fname.startswith("__") and fname.endswith("__"):
                continue
            test_code = _generate_stub(mod.get("path", "unknown.py"), func)
            tests.append({
                "target_file": mod.get("path", "unknown.py"),
                "function": fname,
                "test_code": test_code,
            })

    gemini_enabled = bool(get_gemini_api_key())
    print(f"[TEST_WRITER] Gemini API available: {gemini_enabled}", file=sys.stderr)
    print(f"[TEST_WRITER] Total stubs generated: {len(tests)}", file=sys.stderr)

    if gemini_enabled and tests:
        print(f"[TEST_WRITER] Enhancing up to 30 tests with Gemini 1.5 Flash...", file=sys.stderr)
        tests = _llm_enhance(tests, analysis)
    else:
        print("[TEST_WRITER] Skipping LLM enhancement (no key or no tests)", file=sys.stderr)

    return {**state, "tests": tests}


def _generate_stub(filepath: str, func_info: dict) -> str:
    """Create a basic pytest stub for a function."""
    func_name = func_info.get("name", "unknown_function")
    args = func_info.get("args", []) or []
    docstring = func_info.get("docstring", "")
    lineno = func_info.get("lineno", "?")

    module_path = filepath.replace("/", ".").replace("\\", ".")
    if module_path.endswith(".py"):
        module_path = module_path[:-3]

    # Build placeholder argument values
    arg_vals = []
    for arg in args:
        if arg in ("self", "cls"):
            continue
        arg_vals.append("None")

    call_str = f"{func_name}({', '.join(arg_vals)})"

    docstring_comment = f"    # Function docstring: {docstring[:100]}" if docstring else ""

    return textwrap.dedent(f"""\
        # Auto-generated by AgentQA Test Writer Agent
        # Target: {func_name} (line {lineno}) in {filepath}
        import pytest
        {docstring_comment}
        # TODO: adjust import path — e.g.: from {module_path} import {func_name}

        def test_{func_name}_normal():
            # Normal input — happy path
            assert True  # replace with: result = {call_str}; assert result ...

        def test_{func_name}_boundary():
            # Boundary values (zero, max, min, edge lengths)
            assert True

        def test_{func_name}_null_empty():
            # Null / empty inputs
            assert True

        def test_{func_name}_type_mismatch():
            # Type mismatch — wrong argument types
            assert True

        def test_{func_name}_logic_edge():
            # Logic-specific edge case
            assert True
    """)


